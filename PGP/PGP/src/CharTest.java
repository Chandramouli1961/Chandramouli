
public class CharTest {

	public static void main(String[] args) {
		char c = 'A';
		System.out.println(c); //A
		
		c = 65;
		System.out.println(c); //A
		
		c = '\u0041';
		System.out.println(c); //A
		
		c = '\u0905';
		System.out.println(c); 
		
		c = '\u4eca';
		System.out.println(c);
		
		int n = 'A';
		System.out.println(n);
	}
}
